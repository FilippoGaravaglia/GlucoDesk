    /// <summary>
    /// Gets the supported application languages.
    /// </summary>
    public static IReadOnlyList<AppLanguageOption> SupportedLanguages { get; } =
    [
        new AppLanguageOption("en", "English", "English"),
        new AppLanguageOption("it", "Italian", "Italiano")
    ];

    /// <summary>
    /// Gets the canonical translation keys.
    /// </summary>
    public static IReadOnlyCollection<string> Keys => English.Keys.ToArray();

    /// <summary>
    /// Normalizes a language code to a supported GlucoDesk language code.
    /// </summary>
    /// <param name="languageCode">The requested language code.</param>
    /// <returns>The normalized supported language code.</returns>
    public static string NormalizeLanguageCode(string? languageCode)
    {
        if (string.IsNullOrWhiteSpace(languageCode))
        {
            return DefaultLanguageCode;
        }

        var normalizedCode = languageCode.Trim().ToLowerInvariant();

        if (normalizedCode.StartsWith("it", StringComparison.Ordinal))
        {
            return "it";
        }

        if (normalizedCode.StartsWith("en", StringComparison.Ordinal))
        {
            return "en";
        }

        return DefaultLanguageCode;
    }

    /// <summary>
    /// Gets the translations for the requested language code.
    /// </summary>
    /// <param name="languageCode">The requested language code.</param>
    /// <returns>The localized string dictionary.</returns>
    public static IReadOnlyDictionary<string, string> GetTranslations(string? languageCode)
    {
        return NormalizeLanguageCode(languageCode) switch
        {
            "it" => Italian,
            _ => English
        };
    }

    /// <summary>
    /// Translates a key using the selected language.
    /// </summary>
    /// <param name="languageCode">The selected language code.</param>
    /// <param name="key">The translation key.</param>
    /// <returns>The translated text, or the key when missing.</returns>
    public static string Translate(string? languageCode, string key)
    {
        var translations = GetTranslations(languageCode);

        if (translations.TryGetValue(key, out var text))
        {
            return text;
        }

        if (English.TryGetValue(key, out var fallbackText))
        {
            return fallbackText;
        }

        return key;
    }
