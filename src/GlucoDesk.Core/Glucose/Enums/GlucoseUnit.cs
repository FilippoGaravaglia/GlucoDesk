/// <summary>
/// Represents the unit used to express a glucose value.
/// </summary>
public enum GlucoseUnit
{
    /// <summary>
    /// Milligrams per deciliter.
    /// </summary>
    MgDl = 0,

    /// <summary>
    /// Millimoles per liter.
    /// </summary>
    MmolL = 1
}